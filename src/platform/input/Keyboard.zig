pressed: HashSetUnmanaged(Key) = .empty,
just_pressed: HashSetUnmanaged(Key) = .empty,
just_released: HashSetUnmanaged(Key) = .empty,

pub fn deinit(self: *Self, allocator: std.mem.Allocator) void {
    self.pressed.deinit(allocator);
    self.just_pressed.deinit(allocator);
    self.just_released.deinit(allocator);
}

pub fn isPressed(self: Self, key: Key) bool {
    return self.pressed.contains(key);
}

pub fn justPressed(self: Self, key: Key) bool {
    return self.just_pressed.contains(key);
}

pub fn justReleased(self: Self, key: Key) bool {
    return self.just_released.contains(key);
}
